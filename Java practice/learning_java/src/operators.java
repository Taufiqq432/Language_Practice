public class operators {
    public static void main(String_learning[] args) {
        //arithmatic operators
        int a=15;
        int b=6;
        int sum =a+b;
        int sub=a-b;
        int multi=a*b;
        int divided= a/b;
        int remainder= a%b;
        System.out.println(sum+"and"+sub+"\n"+multi+"\n"+divided+"\n"+remainder);
        // comparison operator
        System.out.println(64>8);
        System.out.println(4==6);
        //assignment operator
        System.out.println(a+=6);
        System.out.println(b*=3);
        //logical operator
        System.out.println(67>5&&57<8);
        System.out.println(67>5||57<8);
        float c= 10 ;
        float d= 3;
        float e = c/d;
        System.out.println(e);


    }
}
